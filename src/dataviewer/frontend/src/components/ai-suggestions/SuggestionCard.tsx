/**
 * Card displaying AI annotation suggestions with accept/reject actions.
 */

import { cn } from '@/lib/utils';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardFooter, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Progress } from '@/components/ui/progress';
import { Separator } from '@/components/ui/separator';
import {
  Check,
  X,
  Sparkles,
  AlertTriangle,
  Star,
  Flag,
  ChevronDown,
  ChevronUp,
  Info,
} from 'lucide-react';
import { useState } from 'react';
import type { AnnotationSuggestion, DetectedAnomaly } from '@/api/ai-analysis';

export interface SuggestionCardProps {
  /** AI suggestion data */
  suggestion: AnnotationSuggestion;
  /** Whether the suggestion is being applied */
  isApplying?: boolean;
  /** Whether the suggestion was accepted */
  isAccepted?: boolean;
  /** Whether the suggestion was rejected */
  isRejected?: boolean;
  /** Accept handler */
  onAccept?: () => void;
  /** Reject handler */
  onReject?: () => void;
  /** Partial accept handler (apply specific fields) */
  onPartialAccept?: (fields: SuggestionField[]) => void;
  /** Additional class names */
  className?: string;
}

export type SuggestionField =
  | 'task_completion'
  | 'trajectory_quality'
  | 'flags'
  | 'anomalies';

/** Star rating display component */
function StarRatingDisplay({ rating, max = 5 }: { rating: number; max?: number }) {
  return (
    <div className="flex items-center gap-0.5">
      {Array.from({ length: max }, (_, i) => (
        <Star
          key={i}
          className={cn(
            'h-4 w-4',
            i < rating ? 'fill-yellow-400 text-yellow-400' : 'text-gray-300'
          )}
        />
      ))}
    </div>
  );
}

/** Anomaly item display */
function AnomalyItem({ anomaly }: { anomaly: DetectedAnomaly }) {
  const severityColors = {
    low: 'bg-yellow-100 text-yellow-700',
    medium: 'bg-orange-100 text-orange-700',
    high: 'bg-red-100 text-red-700',
  };

  return (
    <div className="flex items-start gap-2 text-sm">
      <AlertTriangle
        className={cn(
          'h-4 w-4 mt-0.5 shrink-0',
          anomaly.severity === 'high'
            ? 'text-red-500'
            : anomaly.severity === 'medium'
              ? 'text-orange-500'
              : 'text-yellow-500'
        )}
      />
      <div className="flex-1 min-w-0">
        <div className="flex items-center gap-2">
          <span className="font-medium truncate">{anomaly.type.replace(/_/g, ' ')}</span>
          <Badge variant="outline" className={cn('text-xs', severityColors[anomaly.severity])}>
            {anomaly.severity}
          </Badge>
        </div>
        <p className="text-xs text-muted-foreground truncate">{anomaly.description}</p>
        <p className="text-xs text-muted-foreground">
          Frames {anomaly.frame_start} - {anomaly.frame_end}
        </p>
      </div>
    </div>
  );
}

/**
 * Displays AI suggestion with accept/reject actions.
 */
export function SuggestionCard({
  suggestion,
  isApplying = false,
  isAccepted = false,
  isRejected = false,
  onAccept,
  onReject,
  onPartialAccept,
  className,
}: SuggestionCardProps) {
  const [isExpanded, setIsExpanded] = useState(false);
  const [selectedFields, setSelectedFields] = useState<Set<SuggestionField>>(
    new Set(['task_completion', 'trajectory_quality', 'flags', 'anomalies'])
  );

  const toggleField = (field: SuggestionField) => {
    const newSet = new Set(selectedFields);
    if (newSet.has(field)) {
      newSet.delete(field);
    } else {
      newSet.add(field);
    }
    setSelectedFields(newSet);
  };

  const handlePartialAccept = () => {
    if (onPartialAccept) {
      onPartialAccept(Array.from(selectedFields));
    }
  };

  const confidencePercent = Math.round(suggestion.confidence * 100);

  return (
    <Card
      className={cn(
        'overflow-hidden transition-all',
        isAccepted && 'border-green-300 bg-green-50/50',
        isRejected && 'border-red-300 bg-red-50/50 opacity-60',
        className
      )}
    >
      <CardHeader className="pb-2">
        <div className="flex items-center justify-between">
          <CardTitle className="flex items-center gap-2 text-base">
            <Sparkles className="h-4 w-4 text-blue-500" />
            AI Suggestion
          </CardTitle>
          <div className="flex items-center gap-2">
            <span className="text-xs text-muted-foreground">
              {confidencePercent}% confidence
            </span>
            <Progress value={confidencePercent} className="w-16 h-1.5" />
          </div>
        </div>
      </CardHeader>

      <CardContent className="space-y-3">
        {/* Task Completion */}
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-2">
            {onPartialAccept && (
              <input
                type="checkbox"
                checked={selectedFields.has('task_completion')}
                onChange={() => toggleField('task_completion')}
                className="h-4 w-4 rounded border-gray-300"
              />
            )}
            <span className="text-sm font-medium">Task Completion</span>
          </div>
          <StarRatingDisplay rating={suggestion.task_completion_rating} />
        </div>

        {/* Trajectory Quality */}
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-2">
            {onPartialAccept && (
              <input
                type="checkbox"
                checked={selectedFields.has('trajectory_quality')}
                onChange={() => toggleField('trajectory_quality')}
                className="h-4 w-4 rounded border-gray-300"
              />
            )}
            <span className="text-sm font-medium">Trajectory Quality</span>
          </div>
          <StarRatingDisplay rating={suggestion.trajectory_quality_score} />
        </div>

        {/* Flags */}
        {suggestion.suggested_flags.length > 0 && (
          <div className="space-y-1">
            <div className="flex items-center gap-2">
              {onPartialAccept && (
                <input
                  type="checkbox"
                  checked={selectedFields.has('flags')}
                  onChange={() => toggleField('flags')}
                  className="h-4 w-4 rounded border-gray-300"
                />
              )}
              <span className="text-sm font-medium">Suggested Flags</span>
            </div>
            <div className="flex flex-wrap gap-1 ml-6">
              {suggestion.suggested_flags.map((flag) => (
                <Badge key={flag} variant="outline" className="text-xs">
                  <Flag className="h-3 w-3 mr-1" />
                  {flag.replace(/_/g, ' ')}
                </Badge>
              ))}
            </div>
          </div>
        )}

        {/* Anomalies */}
        {suggestion.detected_anomalies.length > 0 && (
          <div className="space-y-2">
            <div className="flex items-center justify-between">
              <div className="flex items-center gap-2">
                {onPartialAccept && (
                  <input
                    type="checkbox"
                    checked={selectedFields.has('anomalies')}
                    onChange={() => toggleField('anomalies')}
                    className="h-4 w-4 rounded border-gray-300"
                  />
                )}
                <span className="text-sm font-medium">
                  Detected Anomalies ({suggestion.detected_anomalies.length})
                </span>
              </div>
              <Button
                variant="ghost"
                size="sm"
                onClick={() => setIsExpanded(!isExpanded)}
                className="h-6 px-2"
              >
                {isExpanded ? (
                  <ChevronUp className="h-4 w-4" />
                ) : (
                  <ChevronDown className="h-4 w-4" />
                )}
              </Button>
            </div>
            {isExpanded && (
              <div className="ml-6 space-y-2">
                {suggestion.detected_anomalies.map((anomaly) => (
                  <AnomalyItem key={anomaly.id} anomaly={anomaly} />
                ))}
              </div>
            )}
          </div>
        )}

        {/* Reasoning */}
        <Separator className="my-2" />
        <div className="flex items-start gap-2 text-xs text-muted-foreground">
          <Info className="h-3 w-3 mt-0.5 shrink-0" />
          <p>{suggestion.reasoning}</p>
        </div>
      </CardContent>

      <CardFooter className="gap-2 pt-2">
        {isAccepted ? (
          <div className="flex items-center gap-2 text-green-600 text-sm">
            <Check className="h-4 w-4" />
            <span>Applied</span>
          </div>
        ) : isRejected ? (
          <div className="flex items-center gap-2 text-red-600 text-sm">
            <X className="h-4 w-4" />
            <span>Rejected</span>
          </div>
        ) : (
          <>
            <Button
              variant="outline"
              size="sm"
              onClick={onReject}
              disabled={isApplying}
              className="text-red-600 hover:text-red-700 hover:bg-red-50"
            >
              <X className="h-4 w-4 mr-1" />
              Reject
            </Button>
            {onPartialAccept && selectedFields.size < 4 ? (
              <Button
                size="sm"
                onClick={handlePartialAccept}
                disabled={isApplying || selectedFields.size === 0}
                className="flex-1"
              >
                <Check className="h-4 w-4 mr-1" />
                Apply Selected ({selectedFields.size})
              </Button>
            ) : (
              <Button
                size="sm"
                onClick={onAccept}
                disabled={isApplying}
                className="flex-1"
              >
                <Check className="h-4 w-4 mr-1" />
                Apply All
              </Button>
            )}
          </>
        )}
      </CardFooter>
    </Card>
  );
}
